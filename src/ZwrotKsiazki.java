    private void utwozListe(){

        try{
        String sql="select IDksiazki from WydaneKsiazki where IDstudenta=?";            
            oswiadczenie = polaczenie.prepareStatement(sql);
            oswiadczenie.setString(1, Album.getText());
            result = oswiadczenie.executeQuery();
            
            while(result.next()){
                String item = result.getString("IDksiazki");
                listaKsiazek.addItem(item);
            }
        } catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        } finally{
            try{
               result.close();
               oswiadczenie.close();
            } catch(Exception ex){            
            }   
        }            
    }
    
    public void Usunac(){
        String sql="delete from WydaneKsiazki where IDksiazki=?";
        try{
           oswiadczenie = polaczenie.prepareStatement(sql);
           oswiadczenie.setString(1, listaKsiazek.getSelectedItem().toString());
           oswiadczenie.executeUpdate();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
