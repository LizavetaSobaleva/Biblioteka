    private void btnKsiazkaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKsiazkaActionPerformed
        // TODO add your handling code here:
        String sql="select nazwa,autor,rokWydania,iloscStron,dataWydania from WydaneKsiazki where IDksiazki=?";
        try{
           oswiadczenie = polaczenie.prepareStatement(sql);
           oswiadczenie.setString(1, listaKsiazek.getSelectedItem().toString());
            result = oswiadczenie.executeQuery();
            if(result.next()){
            String add1= result.getString("nazwa");
            Nazwa.setText(add1);
            String add2= result.getString("autor");
            Autor.setText(add2);
            String add3= result.getString("rokWydania");
            Rok.setText(add3);
            String add4= result.getString("iloscStron");         
            Strony.setText(add4);
            String add5= result.getString("dataWydania");         
            dataWydania.setText(add5);
            result.close();
            oswiadczenie.close();
           }
           else{
               JOptionPane.showMessageDialog(null, "Książka nie została znaleziona");
           }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        } finally{
            try{
               result.close();
               oswiadczenie.close();
            }catch(Exception ex){            
            }
        }      
     
    }//GEN-LAST:event_btnKsiazkaActionPerformed

    private void btnStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStudentActionPerformed
        // TODO add your handling code here:
        String sql="select imie,nazwisko,wydzial,email,IDksiazki from WydaneKsiazki where IDstudenta=?";
        try{
           oswiadczenie = polaczenie.prepareStatement(sql);
           oswiadczenie.setString(1, Album.getText());
           result = oswiadczenie.executeQuery();
           if(result.next()){
            listaKsiazek.removeAllItems();
            String add1 = result.getString("imie");
            Imie.setText(add1);
            String add2 = result.getString("nazwisko");
            Nazwisko.setText(add2);
            String add3 = result.getString("wydzial");
            Wydzial.setText(add3);
            String add4 = result.getString("email");
            Email.setText(add4);
            utwozListe();            
            result.close();
            oswiadczenie.close();
           }
           else{
               JOptionPane.showMessageDialog(null, "Student nie został znaleziony lub nie maję wypozyczonych książek.");
           }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        } finally{
            try{
               result.close();
               oswiadczenie.close();
            }catch(Exception ex){            
            }
        }        
    }//GEN-LAST:event_btnStudentActionPerformed

